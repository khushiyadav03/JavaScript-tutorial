/*
THEORY: HOW ARRAYS & OBJECTS ARE STORED IN MEMORY

1. Dynamic Size Problem in Arrays:
If an array stores different data types (strings, numbers), the bytes required per element fluctuate.
This breaks the formula: Base Address + (Index * Size) = Element Address.
Solution: Instead of storing actual values continuously, the array stores FIXED-SIZE Memory Addresses (Pointers).
- For small integers, the value itself might be encoded directly into the address block (using a special bit flag like ending in 0).
- For strings or complex objects, the array stores an 8-byte pointer that points to another location in the Heap where the actual string is stored.
This ensures O(1) access time because the array blocks remain a fixed size.

2. Array Push Memory Limits:
If an array runs out of contiguous memory space, JS finds a new, larger memory block, copies the data, and updates the "Element Pointer".
Because a constant array variable (const arr = []) only points to the "Map Pointer", updating the internal "Element Pointer" does not violate the 'const' assignment.

3. Object Storage Architecture:
Objects do not store data simply as continuous key-value arrays. If you update a value to a larger string, it would require shifting everything below it in memory.
Solution: JS Engines (like V8) use an architecture involving:
- Map Pointers (Hidden Classes): Stores the structure/shape of the object.
- Property Pointers: Points to the keys/values.
- Element Pointers: Points to numbered properties (like array indices).

Values and Keys (Strings) are stored elsewhere in the Heap, and the object's core structure only holds pointers (memory addresses) to them. This ensures consistency and prevents massive memory shifts when a string value is updated.

4. Optimization (Hash Maps):
If an object has 1000 properties, scanning them sequentially to find 'user.city' would be O(N).
Instead, JS engines implement Hash Maps internally. The key ("city") is hashed to generate a specific memory address, allowing the engine to fetch the property in O(1) constant time without checking every single property.
*/